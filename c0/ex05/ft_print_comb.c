#include <unistd.h>

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = 0 +48;
	while (a <= 7 +48)
	{
		b = a + 1;
		while (b <= 8 +48)
		{
			c = b + 1;
			while (c <= 9 +48)
			{
				write (1, &a, 1);
				write (1, &b, 1);
				write (1, &c, 1);
				if (a != 7 +48)
					write (1, ", ", 2);
				c++;
			}
			b++;
		}
		a++;
	}
}
